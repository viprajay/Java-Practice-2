public class SwitchCase {
    public static void main(String[] args) {
        char a = 'l';
        switch (a) {
            case 'l':
                System.out.println("Lavi");
                break;
            case 'r':
                System.out.println("Rahul");
                break;
            case 's':
                System.out.println("Shresht");
                break;
            case 'v':
                System.out.println("Viprajay");
                break;
            default:
                System.out.println("No match");
        }
    }
}
